            </p>
            <p class="sub-title">
              2.围绕全产业链条推进标准体系建设。打破传统的行业和领域壁垒，打通产业链堵点，深化产业链上中下游标准的协同合作。加快建立健全跨行业、跨领域的标准化协同工作机制，鼓励龙头企业带动上下游配套中小企业共同开展标准研制，推动形成统一协调的标准体系。
