            </p>
            <p class="sub-title">
              14.积极支持中外标准化交流合作。鼓励国内的协会、标准化专业机构等加强与国际同行的标准化交流与合作，聚焦共同关注的领域，形成标准化共识，不断扩大国际标准化工作的朋友圈。支持围绕“一带一路”的建设需要，组织编制行业标准和强制性国家标准外文版，促进我国技术、产品、工程和服务的国际化发展。
            </p>
            <p class="sub-title">
              <strong>
                五、积极推进重点标准的有效实施
              </strong>
            </p>
